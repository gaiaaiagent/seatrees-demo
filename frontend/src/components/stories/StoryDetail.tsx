'use client'

import { X, Copy, Check, Sparkles } from 'lucide-react'
import { useState } from 'react'
import { Badge } from '@/components/ui/badge'
import { Button } from '@/components/ui/button'
import type { Story } from '@/types'

const typeColors: Record<Story['story_type'], string> = {
  pmu: 'bg-emerald-500/20 text-emerald-300 border-emerald-500/30',
  explainer: 'bg-cyan-500/20 text-cyan-300 border-cyan-500/30',
  faq: 'bg-amber-500/20 text-amber-300 border-amber-500/30',
  comparison: 'bg-violet-500/20 text-violet-300 border-violet-500/30',
}

const typeLabels: Record<Story['story_type'], string> = {
  pmu: 'PMU',
  explainer: 'Explainer',
  faq: 'FAQ',
  comparison: 'Comparison',
}

interface StoryDetailProps {
  story: Story
  onClose: () => void
}

export function StoryDetail({ story, onClose }: StoryDetailProps) {
  const [copied, setCopied] = useState(false)

  async function handleCopy() {
    try {
      await navigator.clipboard.writeText(story.content)
      setCopied(true)
      setTimeout(() => setCopied(false), 2000)
    } catch {
      // Fallback: ignore
    }
  }

  return (
    <div className="space-y-4">
      <div className="flex items-start justify-between gap-4">
        <div className="flex flex-wrap items-center gap-2">
          <Badge className={typeColors[story.story_type]}>
            {typeLabels[story.story_type]}
          </Badge>
          <Badge variant="outline" className="text-xs">
            {story.audience}
          </Badge>
        </div>
        <Button
          variant="ghost"
          size="icon-xs"
          onClick={(e) => {
            e.stopPropagation()
            onClose()
          }}
          aria-label="Collapse story"
        >
          <X className="size-4" />
        </Button>
      </div>

      <h3 className="text-xl font-semibold">{story.title}</h3>

      <div className="max-h-96 overflow-y-auto pr-1">
        <p className="text-sm leading-relaxed text-muted-foreground whitespace-pre-line">
          {story.content}
        </p>
      </div>

      <div className="flex items-center justify-between border-t border-border pt-3">
        <Button
          variant="outline"
          size="sm"
          onClick={(e) => {
            e.stopPropagation()
            handleCopy()
          }}
        >
          {copied ? (
            <Check className="size-3.5 text-emerald-400" />
          ) : (
            <Copy className="size-3.5" />
          )}
          {copied ? 'Copied' : 'Copy Full Content'}
        </Button>
        <span className="flex items-center gap-1 text-[10px] text-muted-foreground/60">
          <Sparkles className="size-3" />
          Generated by Regen AI
        </span>
      </div>
    </div>
  )
}
